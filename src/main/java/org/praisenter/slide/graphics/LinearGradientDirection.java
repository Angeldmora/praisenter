public enum LinearGradientDirection {
	/** Top to bottom */
	TOP,
	
	/** Right to left */
	RIGHT,
	
	/** Bottom to top */
	BOTTOM,
	
	/** Left to right */
	LEFT,
	
	// corners
	
	/** Top-left to bottom right */
	TOP_LEFT,
	
	/** Top-right to bottom left */
	TOP_RIGHT,
	
	/** Bottom-left to top right */
	BOTTOM_LEFT,
	
	/** Bottom-right to top left */
	BOTTOM_RIGHT
}